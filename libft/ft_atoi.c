#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	m;
	int	t;

	i = 0;
	m = 1;
	t = 0;
	while (((str[i] == 32)) || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	if ((str[i] == '+') || (str[i] == '-'))
	{
		if (str[i] == '-')
		{
			m = m * -1;
		}
			i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		t = t * 10 + str[i] - '0';
		i++;
	}
	return (m * t);
}
